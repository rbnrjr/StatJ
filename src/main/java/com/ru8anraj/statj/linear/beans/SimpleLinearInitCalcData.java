package com.ru8anraj.statj.linear.beans;

public class SimpleLinearInitCalcData {
    private long meanOfX;
    private long meanOfY;
    private long numerator;
    private long denominator;

    public long getMeanOfX() {
        return meanOfX;
    }

    public void setMeanOfX(long meanOfX) {
        this.meanOfX = meanOfX;
    }

    public long getMeanOfY() {
        return meanOfY;
    }

    public void setMeanOfY(long meanOfY) {
        this.meanOfY = meanOfY;
    }

    public long getNumerator() {
        return numerator;
    }

    public void setNumerator(long numerator) {
        this.numerator = numerator;
    }

    public long getDenominator() {
        return denominator;
    }

    public void setDenominator(long denominator) {
        this.denominator = denominator;
    }
}
